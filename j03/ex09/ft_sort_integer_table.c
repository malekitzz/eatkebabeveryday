void	ft_sort_integer_table(int *tab, int size)
{
	int i;
	int tmp;

	tmp = 0;
	i = 0;
	while (i < size)
	{
		if (tab[i + 1] < tab[i])
		{
			tmp = tab[i];
			tab[i] = tab[i + 1];
			tab[i + 1] = tmp;
			i = 0;
		}
		else
			i++;
	}
}
