int		ft_max(int *tab, int length)
{
	int i;
	int tmp;

	tmp = 0;
	i = 0;
	while (i < length - 1)
	{
		if (tab[i] > tab[i + 1])
		{
			tmp = tab[i + 1];
			tab[i + 1] = tab[i];
			tab[i] = tmp;
			i = 0;
		}
		i++;
	}
	return (tab[length - 1]);
}
