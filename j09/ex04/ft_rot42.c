char *ft_rot42(char *str)
{
	int i;

	i = 0;
	while (str[i])
	{
		if (str[i] >= 'a' && str[i] <= 'j')
			str[i] += 16;
		if (str[i] >= 'k' && str[i] <= 'z')
			str[i] -= 10;
		i++;
	}
	str[i] = '\0';
}

int main ()
{
	char test[] = "coucou";
	printf("%s", ft_rot42(test));

	return (0);
}
	
